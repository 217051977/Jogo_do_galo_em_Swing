public class Shift {

    /*
    * Creates the shiftCounter
    * */
    private int count = 0;

    /*
    * Empty constructor
    * */
    Shift() {}

    /*
    * Get the shiftCounter (count)
    * */
    int getShiftCounter() {

        /*
        * Return the value of the variable count
        * */
        return count;

    }

    /*
    * ShiftCounter Adder
    * */
    void nextShift() {

        /*
        * add one unit to the count variable
        * */
        count++;

    }

}
